CREATE OR REPLACE VIEW "public"."vw_stock_status" WITH ("security_invoker"='on') AS
 SELECT "p"."business_id",
    "p"."id" AS "product_id",
    "p"."name" AS "product_name",
    "p"."unit_cost",
    "c"."id" AS "category_id",
    "c"."name" AS "category_name",
    (COALESCE("i"."stock", (0)::bigint))::integer AS "current_stock",
    (COALESCE("i"."min_stock", (0)::bigint))::integer AS "min_stock",
    "p"."track_stock",
        CASE
            WHEN ("p"."track_stock" = false) THEN 'sin_control'::"text"
            WHEN (COALESCE("i"."stock", (0)::bigint) <= 0) THEN 'sin_stock'::"text"
            WHEN ((COALESCE("i"."stock", (0)::bigint) <= COALESCE("i"."min_stock", (0)::bigint)) AND (COALESCE("i"."min_stock", (0)::bigint) > 0)) THEN 'stock_bajo'::"text"
            ELSE 'con_stock'::"text"
        END AS "stock_status"
   FROM (("public"."products" "p"
     LEFT JOIN "public"."categories" "c" ON (("c"."id" = "p"."category_id")))
     LEFT JOIN "public"."inventory" "i" ON (("i"."product_id" = "p"."id")))
  WHERE ("p"."is_active" = true);


