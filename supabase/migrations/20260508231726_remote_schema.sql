CREATE OR REPLACE FUNCTION "public"."get_dashboard_metrics"() RETURNS json
    LANGUAGE "plpgsql"
    AS $$
declare result json;
begin
  select json_build_object(

    'summary', (
      select json_build_object(
        'numberSalesToday', count(*),
        'totalSalesToday', sum(subtotal)
      )
      from "salesItems"
      where date(created_at) = current_date
    ),
    'inventory', (
      select json_build_object(
        'totaStock', count(*),
        'lowStock', count(*) filter(where stock <= 5)
      )
      from "products"
    )
    
  
  ) into result;
  return result;
  end;
  $$;


